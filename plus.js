function plus(num1, num2){
    let result;
    result=num1+num2;
    return result
}
function subtract(num1, num2){
    let result;
    result=num1-num2;
    return result
}
function multiply(num1, num2){
    let result;
    result=num1*num2;
    return result
} 
function division(num1,num2){
    let result;
    result=num1/num2;
    return result
}